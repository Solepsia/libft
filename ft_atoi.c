#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		r;

	i = 0;
	neg = 1;
	r = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (int)(str[i] - '0');
		i++;
	}
	return (r * neg);
}
